public class Test01 {
	public static void main(String[] args) {
	
	char var1 = 'A';	// 문자 1개 : char
	int var2 = 100;		// 정수 : int
	long var3 = 2147483648L;	// int 범위 초과 점수 : Long(L 필요)
	float var4 = 3.14f;			// 실수(float) : f 필요
	double var5 = 3.14159;		// 실수(double) : 기본값
	boolean var6 = true;		// 논리형 true : boolean
	boolean var7 = false;		// 논리형 false : boolean 
	String var8 = "hello";		// 문자열: String (클래스)
	String var9 = null;			// 참조형의 기본값: null 사용 가능
	
	System.out.println("var1 : "+var1);
	System.out.println("var2 : "+var2);
	System.out.println("var3 : "+var3);
	System.out.println("var4 : "+var4);
	System.out.println("var5 : "+var5);
	System.out.println("var6 : "+var6);
	System.out.println("var7 : "+var7);
	System.out.println("var8 : "+var8);
	System.out.println("var9 : "+var9);
	}	

}
	
