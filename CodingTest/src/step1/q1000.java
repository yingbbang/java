package step1;
import java.util.Scanner;

public class q1000 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in); // 키보드로 입력을 받을 수 있게 준비
		
		int A = sc.nextInt(); // 첫 번째 숫자를 입력받음
		int B = sc.nextInt(); // 두 번째 숫자를 입력받음
		
		System.out.println(A+B); // 두 숫자를 더한 결과를 출력함
	}

}
